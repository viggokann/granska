void SpaceFix(char *s) {
  char *t;
  for (t = s; *s;)
    if (*s != '\n') {
      if (IsSpace(*s)) {
	*t++ = ' ';
	while(IsSpace(*++s));
      } else
	*t++ = *s++;
    } else
      s++;
  *t = '\0';
}

void PunctFix(char *s) {
  char *t;
  for (t = s; *s;)
    if (IsPunct(*s))
      s++;
    else *t++ = *s++;
  *t = '\0';
}

void Space2Punct(char *s) {
  char *t;
  for (t = s; *s; s++, t++)
    if (IsSpace(*s) || *s == '.') {
      *t = '.';
      while(IsSpace(*(s+1)) || IsPunct(*(s+1)))
	s++;
    } else
      *t = *s;
  if (*(t-1) != '.')
    *t++ = '.';
  *t = '\0';
}

